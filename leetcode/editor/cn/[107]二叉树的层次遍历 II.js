/**
 * Definition for a binary tree node.
 * function TreeNode(val) {
 *     this.val = val;
 *     this.left = this.right = null;
 * }
 */
/**
 * @param {TreeNode} root
 * @return {number[][]}
 */
var levelOrderBottom = function(root) {
  let res = []
  function push (node, i) {
    if (node) {
      if (!res[i]) res[i] = []
      res[i].push(node.val)
      push(node.left, i+1)
      push(node.right, i+1)
    }
  }
  push(root, 0)
  return res.reverse()
};
